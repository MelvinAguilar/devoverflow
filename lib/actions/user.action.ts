export async function getAllUsers(params: GetAllUsersParams) {
  try {
    connectToDb();

    const { searchQuery, filter, page = 1, pageSize = 15 } = params;

    // Pagination: Skip the first n results and limit the results to the page size
    const skipAmount = (page - 1) * pageSize;

    // Query: Search for the search query in the name or username
    const query: FilterQuery<typeof User> = {};

    if (searchQuery) {
      query.$or = [
        { name: { $regex: new RegExp(searchQuery, "i") } },
        { username: { $regex: new RegExp(searchQuery, "i") } },
      ];
    }

    // Sort: Sort by the selected filter
    let sortOptions = {};

    switch (filter) {
      case "new_users":
        sortOptions = { joinedAt: -1 };
        break;
      case "old_users":
        sortOptions = { joinedAt: 1 };
        break;
      case "top_contributors":
        sortOptions = { reputation: -1 };
        break;
    }

    // Get the users
    const users = await User.find(query)
      .skip(skipAmount)
      .limit(pageSize)
      .sort(sortOptions);

    // Check if there are more users
    const totalUsers = await User.countDocuments(query);
    const isNext = totalUsers > skipAmount + users.length;

    return { users, isNext };
  } catch (error) {
    console.log(error);
    throw error;
  }
}

